package com.ghc.wz;

import java.lang.Math;

// Mathematics
public class Maths
{
	public static double sin(double x)
	{
		return Math.sin(x);
	}
	public static double cos(double x)
	{
		return Math.cos(x);
	}
	public static double sqrt(double x)
	{
		return Math.sqrt(x);
	}
	public static double rand()
	{
		return Math.random();
	}
	public static double rand(double sc)
	{
		return rand() * sc;
	}
}

